let num1 = parseInt(prompt('digite o primeiro número'))
let num2 = parseInt(prompt('digite o segundo número'))

let soma = num1 + num2
let sub = num1 - num2
let mult = num1 * num2
let div = num1 / num2

alert(`Resultado:
${num1} + ${num2} = ${soma}
${num1} - ${num2} = ${sub}
${num1} * ${num2} = ${mult} 
${num1} / ${num2} = ${div} `)
